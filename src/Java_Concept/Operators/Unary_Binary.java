package Java_Concept.Operators;

public class Unary_Binary {
    /*
    * Unary - Single operand (single variable)
    * Binary - Two operand (two variable)
    * */
    public static void main(String[] args) {
        // unary ! ++ -- += -= *= /= %=
        int a = 10;
        int b = 20;

        // binary + - * / % > < >= <= != && ||
        int c = a + b;
        boolean d = a < b;

    }
}
